public class PrePostIncrement {
    public static void main(String[] args) {
        int a=10;
        System.out.println(a);
        System.out.println("Post Increment.....");
        System.out.println(a++);
        System.out.println(++a);

        System.out.println("decrement...");
        System.out.println(a--);
        System.out.println(--a);
    }
}
