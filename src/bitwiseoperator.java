public class bitwiseoperator {
    public static void main(String[] args) {
        int a=5;
        int b=7;
        System.out.println("AND "+(a & b));
        System.out.println("OR "+(a | b));
        System.out.println("X OR "+(a ^ b));
        System.out.println("Beetwise "+(~a));//beetwise operator can be perform on only one operator
    }
}
